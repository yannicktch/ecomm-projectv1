        const appearance = '{{ $appearance ?? "light" }}'; // Now defaults to "light"

        // Add the logic to apply the 'dark' class based on the 'appearance' variable
        // This is the crucial part that's currently missing from your snippet
        if (appearance === 'dark' || (appearance === 'system' && window.matchMedia('(prefers-color-scheme: dark)').matches)) {
            document.documentElement.classList.add('dark');
        } else {
            document.documentElement.classList.remove('dark');
        }
    })();
</script>

